package com.MuhammadZikri;

import java.util.Scanner;

public class test25_while_loops {
    public static void main(String[] args){

        for (int i = 5; i>0 ; i--)
            System.out.println(i);

        int i = 0;
        while (i>0) {
            System.out.println(i);
            i--;
        }

    }
}
