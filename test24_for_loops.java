package com.MuhammadZikri;

public class test24_for_loops {
    public static void main(String[] args){
        for (int i = 0; i < 5 ; i++) { // it will repeat for 0,1,2,3,4 iteration ==> 5 times. As long as i < 5
            System.out.println("HELLO");
            System.out.println("ZIKRI");
        }

        for (int j = 0; j< 5 ; j++)
            System.out.println(j);

        for (int k = 5; k >= 0 ; k--)
            System.out.println(k);




    }
}
